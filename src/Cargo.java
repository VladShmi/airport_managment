/**
 * El enum Cargo representa los diferentes cargos disponibles en una aerolínea.
 */
public enum Cargo {
    /**
     * Piloto del avión.
     */
    PILOTO,

    /**
     * Azafata del avión.
     */
    AZAFATA,

    /**
     * Comisario del avión.
     */
    COMISARIO,

    /**
     * Operador de comunicaciones del avión.
     */
    OPERADOR_COMUNICACIONES;
}
